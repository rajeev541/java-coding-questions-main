import java.util.Scanner;
public class UseOfTryCat {
  
    public static void main(String[] args) {
       Scanner sc = new Scanner(System.in);
       int n1 = sc.nextInt();
       int n2 = sc.nextInt();

       System.out.println("add="+(n1+n2));
       System.out.println("div="+(n1/n2));
       System.out.println("mul="+(n1*n2));
    }

}
